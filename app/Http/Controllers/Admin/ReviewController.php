    public function change_status(Request $request){
         $rvid=$request->rvid;
         $review=Review::where('id',$rvid)->firstOrFail();
         if($review->status=="approved"){
            $review->status="unapproved";
            $msg="review is successfully removed from approved list.";
         }else{
            $review->status="approved";
            $msg="review is successfully approved.";
         }
         $review->save();
         return response()->json(['message'=>$msg,'rs'=>$review->status]);
    }
